circle_rad = 4.6
pi = 3.141

circle_perim = 2 * pi * circle_rad
circle_area = pi * (circle_rad ** 2)

print("The perimeter of a circle with a radius of {}, is {}.".format(circle_rad, circle_perim))
print("The area of a circle with a radius of {}, is {}.".format(circle_rad, circle_area))
